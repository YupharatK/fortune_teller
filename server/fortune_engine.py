# ════════════════════════════════════════════════════════════════════
#   ส่วนที่ 4: ฟังก์ชันหลัก — generate_fortune()
#
#   จุดรวมของทุกอย่าง: รับข้อมูลผู้ใช้ → สร้างผลทำนาย → คืนค่า dict
#   ถูกเรียกจาก server.py เมื่อ Client ส่งคำขอทำนายเข้ามา
#   ผลลัพธ์ dict จะถูกแปลงเป็น JSON ส่งกลับ Client ผ่าน TCP socket
# ════════════════════════════════════════════════════════════════════

def generate_fortune(name: str, birthdate: str, category: str) -> dict:
    """
    ฟังก์ชันหลัก: สร้างผลทำนายดวงทั้งหมด

    รับ  : name      — ชื่อผู้ใช้ (เช่น "สมชาย ใจดี")
           birthdate — วันเกิด "YYYY-MM-DD" (เช่น "1990-04-05")
           category  — หมวดดวง ("love"/"work"/"money"/"study")
    คืนค่า: dict ผลทำนายพร้อมส่งกลับ Client (16 fields)

    Flow:
    ┌─────────────────────────────────────────────┐
    │ 1. ตรวจสอบ category (fallback → "love")      │
    │ 2. get_zodiac() → หาราศี+ธาตุ                │
    │ 3. เลือก template จาก FORTUNES[cat][element] │
    │ 4. get_lucky_info() → สิ่งมงคล               │
    │ 5. รวม dict → return                         │
    └─────────────────────────────────────────────┘

    ถูกเรียกจาก: server.py (handle_client)
    → ผลลัพธ์ส่งกลับ Client → แสดงในหน้า Result (show_result)
    """

    # ── ขั้นตอนที่ 0: ตรวจสอบ category ──────────────────
    # ถ้า category ไม่อยู่ใน FORTUNES → fallback เป็น "love"
    if category not in FORTUNES:
        category = "love"

    # ── ขั้นตอนที่ 1: คำนวณราศีจากวันเกิด ───────────────
    zodiac    = get_zodiac(birthdate)       # dict ข้อมูลราศีทั้งหมด
    element   = zodiac["element"]          # ธาตุ เช่น "ไฟ 🔥"
    zodiac_th = zodiac["name_th"]          # เช่น "ราศีสิงห์ ♌"

    # ── ขั้นตอนที่ 2: เลือก template ตามหมวด × ธาตุ ─────
    # สร้าง seed จาก ชื่อ+วันเกิด+หมวด
    # → คนเดิม + วันเกิดเดิม + หมวดเดิม = ได้ผลดวงเดิมทุกครั้ง (deterministic)
    seed = sum(ord(c) for c in name + birthdate + category)
    random.seed(seed)

    # ดึง template list จาก FORTUNES[category][element]
    # ถ้าไม่มีธาตุตรง → fallback ใช้ธาตุ "ดิน 🌿"
    templates    = FORTUNES[category].get(element, FORTUNES[category]["ดิน 🌿"])
    # สุ่มเลือก 1 template แล้วแทน {name} และ {zodiac_th}
    fortune_text = random.choice(templates).format(
        name      = name,
        zodiac_th = zodiac_th.replace("ราศี", "").strip()   # ตัด "ราศี" ออก เช่น "สิงห์ ♌"
    )

    # ── ขั้นตอนที่ 3: สร้างข้อมูลสิ่งมงคล ───────────────
    lucky = get_lucky_info(category, birthdate)
    random.seed()                           # คืน random กลับปกติ (ป้องกันผลข้างเคียง)

    # ── ขั้นตอนที่ 4: รวมผลลัพธ์ทั้งหมดเป็น dict ────────
    # dict นี้จะถูก json.dumps() ใน server.py แล้วส่งกลับ Client
    return {
        "status":        "success",                              # สถานะ: สำเร็จ
        "name":          name,                                   # ชื่อผู้ใช้
        "birthdate":     birthdate,                              # วันเกิด
        "category":      category,                               # หมวดดวง (key ภาษาอังกฤษ)
        "category_th":   CATEGORY_NAMES[category],               # ชื่อหมวดภาษาไทย เช่น "ดวงความรัก ❤️"
        "zodiac":        zodiac_th,                              # ราศี เช่น "ราศีสิงห์ ♌"
        "zodiac_en":     zodiac["name_en"],                      # ราศีอังกฤษ เช่น "Leo"
        "zodiac_symbol": zodiac["symbol"],                       # สัญลักษณ์ เช่น "♌"
        "element":       element,                                # ธาตุ เช่น "ไฟ 🔥"
        "trait":         zodiac["trait"],                         # นิสัย เช่น "มั่นใจ มีเสน่ห์..."
        "fortune":       fortune_text,                           # ข้อความทำนายดวง (เนื้อหาหลัก)
        "lucky_number":  lucky["lucky_number"],                  # เลขนำโชค (1-9)
        "lucky_color":   lucky["lucky_color"],                   # สีมงคล
        "lucky_day":     lucky["lucky_day"],                     # วันมงคล
        "lucky_gem":     lucky["lucky_gem"],                     # พลอยมงคล
        "lucky_item":    lucky["lucky_item"],                    # ของมงคล
        "generated_at":  datetime.now().strftime("%d/%m/%Y %H:%M"),  # วันเวลาที่ทำนาย
    }


