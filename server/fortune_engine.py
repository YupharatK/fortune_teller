def get_zodiac(birthdate: str) -> dict:
    """
    หาราศีจากวันเกิด

    รับ  : birthdate — string "YYYY-MM-DD" (เช่น "1990-04-05")
    คืนค่า: dict ข้อมูลราศี {name_th, name_en, symbol, element, trait}

    ถูกเรียกจาก: generate_fortune() ขั้นตอนที่ 1
    → ส่งผลต่อ: การเลือก template ทำนาย (เลือกตาม element/ธาตุ)
    """
    try:
        # แปลง string → datetime เพื่อดึงเดือนและวัน
        dt    = datetime.strptime(birthdate, "%Y-%m-%d")
        month = dt.month   # เดือนเกิด (1-12)
        day   = dt.day     # วันเกิด (1-31)
    except ValueError:
        # ถ้าวันเกิดไม่ถูกรูปแบบ → คืนค่า default "ไม่ทราบราศี"
        return _default_zodiac()

    # วนเทียบกับตารางราศีทั้ง 12 ราศี
    for sm, sd, em, ed, name_th, name_en, symbol in ZODIAC_LIST:
        if sm > em:
            # กรณีราศีที่คาบ 2 ปี (มังกร: ธ.ค. → ม.ค.)
            # ต้องเช็คทั้ง "เดือนท้ายปี" และ "เดือนต้นปี" แยกกัน
            if (month == sm and day >= sd) or (month == em and day <= ed):
                return _build_zodiac(name_th, name_en, symbol)
        else:
            # กรณีราศีปกติ (อยู่ในปีเดียวกัน)
            if (month == sm and day >= sd) or (month == em and day <= ed):
                return _build_zodiac(name_th, name_en, symbol)

    # ถ้าไม่ตรงกับราศีใดเลย (ไม่น่าเกิดขึ้น) → คืน default
    return _default_zodiac()


