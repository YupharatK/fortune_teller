# ════════════════════════════════════════════════════════════════════

def calc_lucky_number(birthdate: str) -> int:
    """
    คำนวณเลขนำโชคด้วย Numerology (รวมตัวเลขจนเหลือหลักเดียว)

    รับ  : birthdate — string "YYYY-MM-DD"
    คืนค่า: int 1-9 (เลขนำโชค)

    ตัวอย่าง: "1990-04-05" → 1+9+9+0+0+4+0+5 = 28 → 2+8 = 10 → 1+0 = 1

    ถูกเรียกจาก: get_lucky_info()
    """
    # ดึงเฉพาะตัวเลข (ข้าม '-' ในวันเกิด)
    digits = [int(c) for c in birthdate if c.isdigit()]
    total  = sum(digits)

    # ลดทอนจนเหลือ 1 หลัก (Numerology reduction)
    while total > 9:
        total = sum(int(c) for c in str(total))

    # ถ้าผลลัพธ์เป็น 0 (ไม่น่าเกิดขึ้น) → ใช้ 9 แทน
    return total if total != 0 else 9


